public class User
{
    string name;
    string surname;
    string eMail;
    string password;
    int telephone;

    public User(string name, string surname, string eMail, string password, int telephone)
    {
        this.name = name;
        this.surname = surname;
        this.eMail = eMail;
        this.password = password;
        this.telephone = telephone;
    }
}


